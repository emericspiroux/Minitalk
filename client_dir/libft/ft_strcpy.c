#include	"libft.h"

char	*ft_strcpy(char *s1, char const *s2)
{
	char	*ptr;

	ptr = s1;
	while (*s1 != '\0' || *s2 != '\0')
	{
		*s1++ = *s2++;
	}
	return (ptr);
}
